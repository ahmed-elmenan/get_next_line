#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = s1;
	while (*tmp)
		tmp++;
	while (*s2 && i++ < n)
		(*tmp++) = (*s2++);
	*tmp = '\0';
	return (s1);
}
